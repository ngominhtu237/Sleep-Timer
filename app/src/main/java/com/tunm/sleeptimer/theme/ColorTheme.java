package com.tunm.sleeptimer.theme;

public class ColorTheme {
    private int mPrimaryColor;
    private int mHourSeekBarColor;
    private int mMinuteSeekBarColor;

    public int getPrimaryColor() {
        return mPrimaryColor;
    }

    public void setPrimaryColor(int mPrimaryColor) {
        this.mPrimaryColor = mPrimaryColor;
    }

    public int getHourSeekBarColor() {
        return mHourSeekBarColor;
    }

    public void setmHourSeekBarColor(int mHourSeekBarColor) {
        this.mHourSeekBarColor = mHourSeekBarColor;
    }

    public int getMinuteSeekBarColor() {
        return mMinuteSeekBarColor;
    }

    public void setMinuteSeekBarColor(int mMinuteSeekBarColor) {
        this.mMinuteSeekBarColor = mMinuteSeekBarColor;
    }
}
